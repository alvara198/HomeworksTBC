a = float(input("შეიყვანეთ სამკუთხედის პირველი გვერდის სიგრძე: "))

b = float(input("შეიყვანეთ სამკუთხედის მეორე გვერდის სიგრძე: "))

c = float(input("შეიყვანეთ სამკუთხედის მესამე გვერდის სიგრძე: "))

p = (a + b + c)/2

s = (p*(p-a)*(p-b)*(p-c))**0.5

if( a >= b + c or b >= a + c or c >= a + b  ) :
    print("სამკუთხედი მოცემული გვერდებით არ არსებობს!")
else:
    print(f"მოცემული სამკუთხედის პერიმეტრი არის {p*2} \nმოცემული სამკუთხედის ფართობი არის {s}")
